 * @mixin \Eloquent
 */
class Notification extends BaseModel
{

    public static function deleteNotification($notifyData, $id)
    {
        Notification::whereIn('type', $notifyData)
            ->whereNull('read_at')
            ->where('data', 'like', '{"id":' . $id . ',%')
            ->delete();
    }

}
